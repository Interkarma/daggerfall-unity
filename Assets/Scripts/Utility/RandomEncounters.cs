        // Enemy selection method from classic. Returns an enemy ID based on environment and player level.
        public static MobileTypes ChooseRandomEnemy(bool chooseUnderWaterEnemy)
        {
            int encounterTableIndex = 0;
            Game.PlayerEnterExit playerEnterExit = Game.GameManager.Instance.PlayerEnterExit;
            PlayerGPS playerGPS = Game.GameManager.Instance.PlayerGPS;

            if (!playerEnterExit || !playerGPS)
                return MobileTypes.None;

            if (chooseUnderWaterEnemy)
                encounterTableIndex = 19;
            else if (playerEnterExit.IsPlayerInsideDungeon)
                encounterTableIndex = ((int)playerEnterExit.Dungeon.Summary.DungeonType);
            else if (playerEnterExit.IsPlayerInsideBuilding)
            {
                DFLocation.BuildingTypes buildingType = playerEnterExit.BuildingType;

                if (buildingType == DFLocation.BuildingTypes.GuildHall)
                    encounterTableIndex = 40;
                else if (buildingType == DFLocation.BuildingTypes.Temple)
                    encounterTableIndex = 41;
                else if (buildingType == DFLocation.BuildingTypes.Palace
                    || buildingType == DFLocation.BuildingTypes.House1)
                    encounterTableIndex = 42;
                else if (buildingType == DFLocation.BuildingTypes.House2)
                    encounterTableIndex = 43;
                else if (buildingType == DFLocation.BuildingTypes.House3)
                    encounterTableIndex = 44;
                else
                    encounterTableIndex = 39;
            }
            else
            {
                int climate = playerGPS.CurrentClimateIndex;
                bool isDay = DaggerfallUnity.Instance.WorldTime.Now.IsDay;

                if (playerGPS.IsPlayerInLocationRect)
                {
                    if (isDay)
                        return MobileTypes.None;

                    // Player in location rectangle, night
                    switch (climate)
                    {
                        case (int)MapsFile.Climates.Desert:
                        case (int)MapsFile.Climates.Desert2:
                            encounterTableIndex = 20;
                            break;
                        case (int)MapsFile.Climates.Mountain:

                        // [OSORKON] I moved Mountain Woods to use Mountain encounter tables in towns at night.
                        case (int)MapsFile.Climates.MountainWoods:
                            encounterTableIndex = 23;
                            break;
                        case (int)MapsFile.Climates.Rainforest:

                        // [OSORKON] I moved Swamps to use Rainforest encounter tables in towns at night.
                        case (int)MapsFile.Climates.Swamp:
                            encounterTableIndex = 26;
                            break;
                        case (int)MapsFile.Climates.Subtropical:
                            encounterTableIndex = 29;
                            break;
                        case (int)MapsFile.Climates.Woodlands:
                            encounterTableIndex = 32;
                            break;
                        case (int)MapsFile.Climates.HauntedWoodlands:
                            encounterTableIndex = 35;
                            break;

                        default:
                            return MobileTypes.None;
                    }
                }
                else
                {
                    if (isDay)
                    {
                        // Player not in location rectangle, day
                        switch (climate)
                        {
                            case (int)MapsFile.Climates.Desert:
                            case (int)MapsFile.Climates.Desert2:
                                encounterTableIndex = 21;
                                break;
                            case (int)MapsFile.Climates.Mountain:

                            // [OSORKON] I moved Mountain Woods to use Mountain encounter tables in daytime wilderness.
                            case (int)MapsFile.Climates.MountainWoods:
                                encounterTableIndex = 24;
                                break;
                            case (int)MapsFile.Climates.Rainforest:

                            // [OSORKON] I moved Swamps to use Rainforest encounter tables in daytime wilderness.
                            case (int)MapsFile.Climates.Swamp:
                                encounterTableIndex = 27;
                                break;
                            case (int)MapsFile.Climates.Subtropical:
                                encounterTableIndex = 30;
                                break;
                            case (int)MapsFile.Climates.Woodlands:
                                encounterTableIndex = 33;
                                break;
                            case (int)MapsFile.Climates.HauntedWoodlands:
                                encounterTableIndex = 36;
                                break;

                            default:
                                return MobileTypes.None;
                        }
                    }
                    else
                    {
                        // Player not in location rectangle, night
                        switch (climate)
                        {
                            case (int)MapsFile.Climates.Desert:
                            case (int)MapsFile.Climates.Desert2:
                                encounterTableIndex = 22;
                                break;
                            case (int)MapsFile.Climates.Mountain:
                                encounterTableIndex = 25;
                                break;
                            case (int)MapsFile.Climates.Rainforest:

                            // [OSORKON] I moved Swamps to use Rainforest encounter tables in nighttime wilderness.
                            case (int)MapsFile.Climates.Swamp:
                                encounterTableIndex = 28;
                                break;
                            case (int)MapsFile.Climates.Subtropical:
                                encounterTableIndex = 31;
                                break;

                            // [OSORKON] I set Mountain Woods to use a new, custom encounter table in nighttime wilderness.
                            case (int)MapsFile.Climates.MountainWoods:
                                encounterTableIndex = 38;
                                break;
                            case (int)MapsFile.Climates.Woodlands:
                                encounterTableIndex = 34;
                                break;
                            case (int)MapsFile.Climates.HauntedWoodlands:
                                encounterTableIndex = 37;
                                break;

                            default:
                                return MobileTypes.None;
                        }
                    }
                }
            }

            // [OSORKON] I erased most of what vanilla had here but I kept this necessary variable.
            RandomEncounterTable encounterTable = EncounterTables[encounterTableIndex];

            // [OSORKON] If the "Powerful Enemies Are:" setting is "More Common" and the player is not using modded encounter
            // tables (which are never longer than 20), randomly pick an enemy from the first 100 slots of the encounter table.
            // When I made powerful enemies less common for BOSSFALL v1.3, I added 40 slots at the end of each encounter table
            // and didn't change the first 100. So, by picking only from the first 100, the "More Common" setting uses v1.2.1
            // enemy rarities. I don't think this setting is balanced so I recommend "Less Common".
            if (DaggerfallUnity.Settings.PowerfulEnemyRarity == 1 && encounterTable.Enemies.Length > 99)
            {
                return encounterTable.Enemies[UnityEngine.Random.Range(0, 100)];
            }

            // [OSORKON] In BOSSFALL there are two types of random spawns. The first are dungeon enemies that spawn at set points
            // in dungeon blocks - those are handled in RDBLayout and not here (they are selected using the same method as in this
            // script). The second are random spawns when player is resting in a dungeon or outside at certain locations and times,
            // and that is what the line below handles. If the "Powerful Enemies Are:" setting is "Less Common" (I recommend using
            // this setting as I think it's the most balanced) or if player is using modded encounter tables, a random enemy is
            // selected from the entire array - this random selection is not altered by any external variables such as player level
            // - and this random selection is how BOSSFALL implements unleveled enemies. How often a specific enemy spawns is
            // determined by probabilities - that enemy has a (amount of times enemy is listed / length of the encounter table)
            // percent chance of spawning every time this function is called. Function call frequency is set in FormulaHelper and
            // has been reduced compared to vanilla DFU.
            return encounterTable.Enemies[UnityEngine.Random.Range(0, encounterTable.Enemies.Length)];
        }
    }
